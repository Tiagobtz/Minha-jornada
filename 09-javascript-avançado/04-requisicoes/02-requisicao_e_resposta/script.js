/* 
Como a web funciona (Requisição e resposta)?

Request = Requisição
-> Cabeçalhos / Headers
   - Aqui é enviado juntamente da requisição informações sobre o navegador, dispositivo, endereço e etc.

-> Corpo / Body
   - Aqui é enviado as informações da requisição como por exemplo um texto que você digitou em algum formulário.
   
Response = Resposta
-> Cabeçalhos / Headers
   - Aqui ficará salvo informações para que quando você volte a solicitar novamente uma requisição a resposta seja ainda mais rápida e precisa.

-> Corpo / Body
   - Aqui é onde virá juntamente da resposta os dados que foram solicitados pela requisição. Como por exemplo o documento HTML de algum site que você acessou.
*/